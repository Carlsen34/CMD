
public class Token {
	
	String simbulo; 
	String lexema;
	int numLiha;
	public String getSimbulo() {
		return simbulo;
	}
	public void setSimbulo(String simbulo) {
		this.simbulo = simbulo;
	}
	public String getLexema() {
		return lexema;
	}
	public void setLexema(String lexema) {
		this.lexema = lexema;
	}
	public int getNumLiha() {
		return numLiha;
	}
	public void setNumLiha(int numLiha) {
		this.numLiha = numLiha;
	}
	 

}
